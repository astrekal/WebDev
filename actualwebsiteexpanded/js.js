function changeToDark() {
    alert("Dark mode will be enabled");
    document.body.style.background = "linear-gradient(#000000, #777777) fixed";
    document.body.style.backgroundSize = "cover";
}

function changeToCrabMode() {
    alert("CRABS!!!");
    document.body.style.backgroundImage = "url(img/crab.gif)";
    document.body.style.backgroundSize = "cover";
}